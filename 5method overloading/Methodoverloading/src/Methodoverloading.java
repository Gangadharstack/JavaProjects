
public class Methodoverloading{
	public int add(int num1,int num2)
	{
		int result;
		result=num1+num2;
		return result;
	}
	public int add(int num1,int num2,int num3)
	{
		int result;
		result=num1+num2+num3;
		return result;
	}
    public double add(double n1,double n2)
    {
    	double result;
    	result=n1+n2;
    	return result;
    }
    
}
